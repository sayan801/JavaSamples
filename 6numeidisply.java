class Displaying
{
public static void main(String args[])
{
System.out.println("Numerical Display");
int i=0;
int j;
for(i=0;i<10;i++)
{
for(j=0;j<i;j++)
{
System.out.print(" ");
System.out.print(i);
}
System.out.print("\n");
}

}

}